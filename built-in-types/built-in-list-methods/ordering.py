lst_sort = [3, 1, 2]
lst_sort.sort()
print(f"sort         : {lst_sort}")

lst_sort_rev = [3, 1, 2]
lst_sort_rev.sort(reverse=True)
print(f"sort(reverse): {lst_sort_rev}")

lst_reverse = [1, 2, 3]
lst_reverse.reverse()
print(f"reverse      : {lst_reverse}")
