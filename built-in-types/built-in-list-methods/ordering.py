# -*- coding: utf-8 -*-

list_sort = [3, 1, 2]
list_sort.sort()
print(f"sort         : {list_sort}")

list_sort_reverse = [3, 1, 2]
list_sort_reverse.sort(reverse=True)
print(f"sort(reverse): {list_sort_reverse}")

list_reverse = [1, 2, 3]
list_reverse.reverse()
print(f"reverse      : {list_reverse}")
